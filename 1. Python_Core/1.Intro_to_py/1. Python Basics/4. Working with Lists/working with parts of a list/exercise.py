# Slicing a List
players = ["Messi", "Haaland", "Neymar", "Mbappe", "Ronaldo", "Lewandoski", "capitan", "seb", "cancelo"]
# the first three players in the list are
print(players[:3])

# three players from the middle of the list are
print(players[3:6]) # to accomplish that, I will have to specify the index I want to include and the index that I want to start excluding

# the last three players in the list
print(players[-3:])